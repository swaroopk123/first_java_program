package uneryoperator;

public class unaryplusoperator {
	
	public static void main(String[] args) {
		int a=90;
		
		++a;
		System.out.println(a);
		a++;
		System.out.println(a);
	
		
		//minus nuary operator
		
		int b=47;
		b--;
		System.out.println(b);
		--b;
		System.out.println(b);
		
		
		
	}
}
